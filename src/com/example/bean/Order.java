package com.example.bean;

public class Order {
 
	
	private String orderId;
	private String goodName;
	private String goodPrice;
	private String goodWeight;
	private String goodSum;
	private String priceKind;
	public String getOrderId() {
		return orderId;
	}
	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}
	public String getGoodName() {
		return goodName;
	}
	public void setGoodName(String goodName) {
		this.goodName = goodName;
	}
	public String getGoodPrice() {
		return goodPrice;
	}
	public void setGoodPrice(String goodPrice) {
		this.goodPrice = goodPrice;
	}
	public String getGoodWeight() {
		return goodWeight;
	}
	public void setGoodWeight(String goodWeight) {
		this.goodWeight = goodWeight;
	}
	public String getGoodSum() {
		return goodSum;
	}
	public void setGoodSum(String goodSum) {
		this.goodSum = goodSum;
	}
	public String getPriceKind() {
		return priceKind;
	}
	public void setPriceKind(String priceKind) {
		this.priceKind = priceKind;
	}
	
	
	
}
